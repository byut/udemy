#
# Big O: O(n)
#
# Implement a Python function called print_items
#
# This function should take a single integer as an argument and
# print out a sequence of numbers from 0 up to, but not
# including, the provided integer.
#
# Exercise: https://ua.udemy.com/course/data-structures-algorithms-python/learn/quiz/5972210
#


def print_items(n):
    for i in range(n):
        print(i)
